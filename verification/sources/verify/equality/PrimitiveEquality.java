package verify.equality;

public class PrimitiveEquality {
    public static boolean boolEq(boolean left, boolean right) {
        return left == right;
    }

    public static boolean boolNe(boolean left, boolean right) {
        return left != right;
    }

    public static boolean byteEq(byte left, byte right) {
        return left == right;
    }

    public static boolean shortEq(short left, short right) {
        return left == right;
    }

    public static boolean charEq(char left, char right) {
        return left == right;
    }

    public static boolean intEq(int left, int right) {
        return left == right;
    }

    public static boolean longEq(long left, long right) {
        return left == right;
    }

    public static boolean floatEq(float left, float right) {
        return left == right;
    }

    public static boolean doubleEq(double left, double right) {
        return left == right;
    }
}
